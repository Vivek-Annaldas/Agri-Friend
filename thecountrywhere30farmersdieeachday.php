		<h1 style="font-weight:bold; text-align:left; color:green">The country where 30 farmers die each day</h1>
	<img src="images/220316041512-indian-farmer-file-02162018.webp" /><br><br>
	<p style="text-align: justify">Editor’s Note: Gunisha Kaur is a physician and human rights researcher. She is an assistant professor at Weill Cornell Medicine, director of the Human Rights Impact Lab, and a Stephen M. Kellen Term Member at the Council on Foreign Relations. The views expressed in this commentary are her own. View more opinion on CNN.<br><br>
(CNN) — Gazing across the pristine fields in the district of Sangrur in northern India, there was little indication families here were struggling. A nearby canal brought water to the wheat and rice crops that were the life source of the village’s farmers.<br><br>
Typical for India, the edges of the canal were littered with empty plastic bags, soda cans, and dark orange pesticide containers that swirled together in a corner where the water met a bridge.<br><br>
But alongside the discarded trash floated something else: a bloated body. It was the third body that had been sighted that day, locals told us.<br><br>

I’ve encountered many devastating scenes like this, since I first started visiting farming families across the state of Punjab almost a decade ago. I go as part of a medical exchange program where American physicians learn how biopsychosocial factors – such as government policies, the environment or economics – impact health globally. Our group also partners with the local Baba Nanak Education Society, to gain a grassroots perspective.<br><br>

In some cases, locals retrieve these bodies and return them to their families. Often, they are not recognizable and left to decompose in the river. By village consensus, the cause of death in this case was suicide. Though I’m not sure the body was ever identified.<br><br>
During these visits, we speak to families affected by the epidemic of farmer suicides. Those left behind – parents, wives and children – share their immense grief in the wake of a death. They describe debt passed from one deceased son to another, resulting in multiple suicides in one family.<br><br>
The families tell us that children, particularly girls, are pulled out of school because they can no longer afford the cost of education. Young girls show us their carefully guarded treasure of dowry goods, woven with their own hands, to decrease the ultimate financial burden on their families.<br><br>

The situation for India’s more than 260 million agricultural workers is dire. Nearly 30 people in the farming sector die by suicide daily, according to the most recent figures available, typically due to overwhelming debt. Indeed in 2020, more than 10,000 people in the agricultural sector ended their own lives, according to government data.<br><br>

India’s economic backbone – its farmers and their families – is in collapse. They face crushing pressures: insurmountable debt, environmental degradation, and extreme rates of cancer linked to exposure to pesticides.<br><br>

This strain is compounded by climate change and extreme weather – from ground water depletion to water shortages and crop damage due to rising temperatures – effects which have been tied to increasing suicides in India.<br><br>

Farming is a notoriously economically unstable industry globally, but farmers in India find themselves buried under densely layered problems. Many are subsistence farmers who are drowning in the volatility caused by the Green Revolution which began in the 1960s as a way of industrializing the agriculture sector with high yielding seeds, mechanized tools and pesticides.<br><br>

In some cases, farmers cannot work their land due to illness linked to the revolution’s pesticides and fertilizers. They are dealing with deep-rooted battles against multinational corporations. And all the while having to take out loans each year to make the agricultural cycle possible.<br><br>

And then, when farmers are unable to get loans from legitimate banks, illegal moneylenders – or loan sharks – step in, charging exorbitant interest rates and creating an inescapable debt-trap for farmers, in some instances pushing them to suicide<br><br>

The state of Punjab where we visit, is known as the breadbasket of India. While it accounts for approximately 3% of India’s arable land, it grows almost 20% of the country’s wheat and 12% of the country’s rice. It is also known as “ground zero” for India’s Green Revolution. What happens in Punjab is a warning sign for the rest of the country. Over the last five years, suicides in the rural Indian State of Punjab increased by over 12 times.<br><br>
But addressing the epidemic of farmer suicide is not just a distant policy problem. In a country of nearly 1.4 billion, where almost 60% of the population relies on agriculture for its livelihood, effectively reforming India’s agricultural sector is quite literally a matter of life and death.<br><br>

A first step for India would be to acknowledge and track farmer suicides as the harbinger of trouble. While it’s clear that Indian farmers are facing obstacles that make it nearly impossible to provide for their families, what we don’t know is the magnitude of the problem.<br><br>

Suicide is illegal in India, with prison time threatened for anyone who attempts or is deemed to have assisted with the act. This policy, a remnant of British colonial rule, makes it exceedingly difficult to collect accurate data.<br><br>

Experts have noted for years that criminalizing suicide results in severe underreporting of its occurrence across the country. The 2017 Mental Healthcare Act attempted to limit the Indian Penal Code’s applicability and to decriminalize suicide, in the hopes of empowering people to receive mental health treatment. But critics of the law have pointed out that these attempts have proven ineffective for two key reasons: Section 309 on suicide remains law, and law enforcement officials continue to enforce it.<br><br>

Even today, India’s suicide statistics – including official statistics on farmer suicide – are published by the National Crime Records Bureau and are based on police reports and investigations. In a culture where mental health and suicide are highly stigmatized, impoverished families are disincentivized from reporting suicide to authorities because the state fails to provide families the support they need and registers their loved ones as criminals.<br><br>

Once acknowledged and documented, multiple potential solutions are available. Increasing mental health services can significantly reduce suicide rates, and is another step towards a solution. ​Informal moneylenders and loan sharks must be investigated, their systems dismantled and more viable loan options provided to farmers.<br><br>

Banning highly hazardous pesticides in farming is also a cost-effective way to prevent suicide, including across India as a whole. Indeed, India has one of the highest suicide rates in the world, with an estimated 170,000 people taking their lives each year, according to the World Health Organization.<br><br>

While civil rights activists have been sounding the alarm over farmer suicides for decades, there has largely been deafening silence from the global community.<br><br>

That was until February 2, 2021, when music icon Rihanna posted a tweet heard around the world. She linked to a CNN article about the Indian government’s violent crackdown on the non-violent “farmers’ protests,” and wrote: “Why aren’t we talking about this?! #FarmersProtest.” Within a day, it was already one of her most popular posts, with nearly 1 million likes. A few hours later, another tweet from climate justice activist Greta Thunberg went viral too.<br><br>
Just like that, the world’s attention focused on the “farmers’ protests” which by that point had already been going on for more than six months and which some had already identified as the largest protest in human history. By early December, more than 250 million people had participated in a nationwide strike, in a show of solidarity.<br><br>

The protest was in response to farming laws aimed at loosening the rules around the sale, pricing and storage of farm produce that protected farmers from an unfettered free market for decades.<br><br>

Some saw the legislation as a necessary move that would finally open up India’s faltering economy by giving farmers more autonomy to set their own prices and sell to private corporations directly. Others saw it as a heavy-handed move that would benefit large companies and spell doom for working class farmers.<br><br>

Protesters against the farm bills flooded the streets of New Delhi, in the midst of a global pandemic and in the face of unrelenting police violence, risking their lives to make their voices heard. Indeed, 700 farmers died while calling for reforms, according to union leaders.<br><br>

Now, more than a year after they first started protesting, the farmers have won their battle with the government. In late November, Indian Prime Minister Narendra Modi announced that he would be repealing the farm bills that launched the farmers’ protests.<br><br>

Many around the world have celebrated this decision, seeing it as grassroots victory over the commercial aspirations of the world’s largest democracy. Some also see it as a source of hope for non-violent, democratic protest in a moment where many are investing in social justice efforts but also losing confidence in them.
<br><br>
While there is much to celebrate, those who know the plight of Indian farmers more intimately understand that India’s decision to repeal the laws is not the end of the struggle – but rather, just the beginning.<br><br>

India needs far more than agricultural reform. Officials can begin by destigmatizing and decriminalizing suicide, by sincerely committing to collecting accurate data on suicide attempt and mortality, and by devoting resources to support the mental health needs of India’s farmers and their families. Because right now, many Indian farmers are killing themselves to put food on the nation’s tables.</p><br>
	
	
