	<h1 style="font-weight:bold; text-align:left; color:green">Farmers in India are using AI for agriculture – here's how they could inspire the world</h1>
	<img src="images/vOkUe-1TxFkRMz83SUSfkwjQ3ZS3Lnmtj21W-mGWxoI.gif"/><br><br>
	<h1 style="font-weight:bold; text-align:left; color:green">Using AI for agriculture innovation</h1>
	<p style="text-align: justify">This is why the World Economic Forum India's Centre for the Fourth Industrial Revolution, in collaboration with India’s Union Ministry of Agriculture and the state of Telangana, launched the AI4AI initiative (AI for Agriculture Innovation). Reflecting the complexity of the challenge, organisations involved come from industry (agri-inputs, consumer, food processing, finance, insurance and technology firms), the startup ecosystem and farmer cooperatives.<br><br>
Over eight months starting June 2020, this endeavour held more than 45 workshops, to discuss the challenges smallholder farmers face and how 4IR could help. These discussions lead to a AI4AI plan that helps smallholder farmers by harnessing the power of new technologies including AI, drones and blockchain.</p><br>
<h1 style="font-weight:bold; text-align:left; color:green">From framework to impact</h1>
<p style="text-align: justify">We tested the AI4AI framework in the Khammam district of Telangana, India, among 7,000 farmers. We involved industry and start-up partners and used state-government data management tools (the agriculture data exchange and the agriculture data management framework) to scale up the initiative among this large group of farmers.<br><br>
Named Saagu Baagu locally, this initiative has transformed chili farming in Khammam district using bot advisory services, soil testing technology, AI-based quality testing and a digital platform to connect buyers and sellers.<br><br>
The pilot took 18 months and three crop cycles to complete. During this time, farmers reported a remarkable surge in net income: $800 per acre in a single crop cycle (6 months), effectively double the average income. The digital advisory services contributed to a 21% increase in chili yield production per acre. Pesticide use fell by 9% and fertilizers dropped by 5%, while quality improvements boosted unit prices by 8%.<br><br>
Saagu Baagu was not only a success for its farmers, it achieved the sustainability and efficiency goals set by AI4AI. As a result, in October 2023, the state government expanded Saagu Baagu to include 500,000 farmers, covering five crops across 10 districts.</p><br>

<h1 style="font-weight:bold; text-align:left; color:green">Unlocking digital agriculture's potential</h1>
<p style="text-align: justify">As much of the global south grapples with the challenges of ensuring food security, mitigating climate change impacts and protecting livelihoods, this Indian agtech initiative shows promising results when using AI for agriculture. Collaboration between governments, industry, philanthropists, innovators and farmers can create national frameworks for implementing digital agriculture programmes that ensure food security, sustainability, and alignment with sustainable development goals.<br><br>

Sharing lessons learned and success stories via these digital platforms gives farmers valuable insights and evidence-based strategies for using AI for agriculture. This can help accelerate innovation and guide global efforts in digital farming, promoting sustainability, inclusivity, efficiency and improved nutrition worldwide.</p><br>

