<p>To motivate farmers to adopt chemical free farming and enhance the reach of natural farming, the Government has formulated National Mission on Natural Farming (NMNF) as a separate and independent scheme from 2023-24 by up scaling the Bhartiya Prakritik Krishi Paddati (BPKP). The scheme is for a duration of 2023-2026.
</p><br>
	 <h1 style="font-weight:bold" id="sssssss1">Natural Farming</h1>
	<hr>
	<p>Natural Farming is a way of chemical free farming based on desi cow and locally available resources, with no chemical fertilizers and pesticides and promotes traditional indigenous practices which give freedom to farmers from externally purchased inputs and is largely based on on-farm biomass recycling with major stress on biomass mulching, use of on-farm desi cow dung-urine formulation; managing pests through diversity, on-farm botanical concoctions and exclusion of all synthetic chemical inputs directly or indirectly and emphasis is given on improving natural nutrient cycling and increase in organic matter in the soil, which can help with climate change resilience and carbon sequestration in soils. 
</p>
<br>	


	 <h1 style="font-weight:bold" id="sssssss2">Mission Objectives</h1>
	<hr>
<ul style="text-align:justify">
<li>• To promote alternative system of farming for freedom from external purchased inputs, cost reduction and thereby increasing income of farmers
</li>
<li>• To popularize integrated agriculture-animal husbandry models based on desi cow and local resources.
</li>
<li>• To collect, validate and document Natural Farming practices being practiced in various parts of the country and to encourage participatory research with farmers on further up scaling
</li>
<li>• To undertake activities for awareness creation, capacity building, promotion and demonstration of Natural Farming.
</li>
<li>• To create standards, certification procedure and branding for Natural Farming products for national and international markets.</li>

</ul><br>
<br>
<h1 style="font-weight:bold" id="sssssss3">Benefits to farmers</h1>
	<hr>
<ul style="text-align:justify">
<li>• Incentive to farmers for one time on-farm manure production infrastructure: A financial assistance of Rs. 15000/- per ha @ Rs. 5000/- per ha/year for three years </li>
<li>• Training of farmers by Champion farmer and CRP: 6 such trainings of one day duration will be organized at village level for all the farmers in a batch of 50 farmers. A sum of Rs. 30,000/- is provided for such trainings @ Rs. 50 per farmer per training. </li>
<li>•  Farmer Producer Organisations (FPO) formation for farmers practicing Natural Farming - 100 FPOs in the Gangetic belt (5 Km Corridor of Ganga Basin) and another 400 FPOs in the rest of the country from the 10,000 FPO scheme being implemented by the Ministry of Agriculture and Farmers' Welfare</li>
<li>• Farmer Field School – First year of the natural farming mission will be devoted to the awareness creation through Farmer Field School (FFS).</li>
</ul><br>

