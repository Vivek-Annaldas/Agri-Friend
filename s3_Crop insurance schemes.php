	 <h1 style="font-weight:bold" id="sss1">Crop insurance schemes in India</h1>
	<hr><br>
	<p>Four insurance schemes are being implemented namely by the government to support farmers.</p>
	<ul style="text-align:justify">
<li>• Pradhan Mantri Fasal Bima Yojana (PMFBY)</li>
<li>• Weather based Crop Insurance Scheme (WBCIS)</li>
<li>• Coconut Palm insurance scheme (CPIS) and</li>
<li>• Pilot Unified Package insurance scheme UPIS) (45 districts).</li>
</ul>
<p>Coverage under PMFBY/WBCIS/CPIS/UPIS is compulsory, if you avail crop loan for notified crops. Coverage is voluntary for non-loanee farmers.</p>
	<br>
<br>	
	 <h1 style="font-weight:bold" id="sss2">Benefits</h1>
	<hr><br>
	
	<table >
  <thead>
    <tr>
      <th>S.NO</th>
      <th>Scheme</th>
      <th>Assistance</th>
    </tr>
  </thead>
  <tbody style="text-align:left" id="myTable">
  <tr>
  <td>1</td>
    <td>Pradhan Mantri Fasal Bima Yojana</td>
    <td><ul style="text-align:justify">
<li>• Insurance protection for food crops, oilseeds and annual horticultural/commercial crops notified by state government.</li>
<li>• Uniform maximum premium for all farmers:</li>
<li>• The difference between actual premium and the rate of Insurance payable by farmers shall be shared equally by the Centre and State.</li>
<li>• Claims of full Sum Insured (SI), without capping or reduction in SI.</li>
<li>• If the sowing is not done due to adverse weather/climate, claims upto 25% of sum insured will be paid for prevented sowing/planting risk.</li>
<li>• When the crop yield is less than the guaranteed yield of notified crops,the claim payment equal to shortfall in yield is payable to all insured farmers.</li>
<li>• If 50% loss in mid season of crop then on account advance payment, upto 25% of likely claims will be paid as immediate relief.</li>
<li>• Losses caused due to inundation, hail storm and landslide would be assessed at individual farm level.</li>
<li>• Post harvest losses assessment for damage to crops in cut and spread in the field up to 14 days on account of cyclonic rain and unseasonal rain in the entire country.</li>
<li>• Use of remote sensing Technology and drones to supplement Crop cutting experiments for faster settlement of claims.</li>
<li>• Implementing agency will be selected by the State Government through bid.
</li>

</ul></td>
    
</tr>
<tr>
  <td>2</td>
    <td>	Weather Based Crop Insurance Scheme (WBCIS)</td>
    <td><ul style="text-align:justify">
	<li>• Insurance protection for notified food crops, oilseeds and horticultural /commercial crops
</li>
	<li>• Uniform maximum premium for all farmers like PMFBY :
</li>
	<li>• The difference between actual premium and the rate of Insurance payable by farmers shall be shared equally by the Centre and State.</li>
	<li>• When the Weather indices (rainfall/temperature/relative humidity/wind speed etc) is different (less/ higher) from the Guaranteed Weather Index of notified crops, the claim payment equal to deviation/shortfall is payable to all insured farmers of notified area.</li>
	<li>• Provision for assessment of losses caused by hailstorm and cloud burst at individual farm level.</li>
	<li>• Implementing agency will be selected by the State Government through bid.</li></ul></td>
    
</tr>
<tr>
  <td>3</td>
    <td>	Coconut Palm Insurance Scheme (CPIS)</td>
    <td><ul style="text-align:justify">
	<li>• Insurance protection for Coconut Palm growers.
</li>
	<li>• Premium rate per palm ranges from Rs. 9.00 (in the plant age group of 4 to 15 years) to Rs. 14.00 (in the plant age group of 16-60 years).
</li>
	<li>• 50-75% subsidy of premium is provided to all types of farmers.</li>
	<li>• When the palm damaged, the claim payment equal to input cost loss damage is payable to the insured in notified areas.</li>
	</ul>
</td>
    
</tr>
<tr>
  <td>4</td>
    <td>Unified Package Insurance Scheme (UPIS) as pilot in 45 districts</td>
    <td><ul style="text-align:justify">
	<li>• To provide financial protection and comprehensive risk coverage of crops, assets, life and student safety to farmers.
</li>
	<li>• Pilot will include 7 section Viz.., - Crop Insurance (PMFBY / WBCIS), Loss of life (Pradhan Mantri Jeevan Jyoti Bima Yojna (PMJJBY)), Accident Insurance (Pradhan Mantri Suraksha Bima Yojana - PMSBY) , Student safety, Household, Agriculture implements and Tractor.</li>
	<li>• Crop insurance will be compulsory. However farmers can choose atleast 2 sections from remaining.</li>
	<li>• Farmers may be able to get all requisite insurance products for farmers through one simple proposal/ application Form and through single window.</li>
	<li>• Two flagship schemes of the Government viz PMSBY & PMJJBY have been included apart from insurance of assets.</li>
	<li>• Pilot scheme will be implemented through single window.</li>
	<li>• Processing of claims (other than Crop Insurance) on the basis of individual claim report.
</li>
	</ul>
</td>
    
</tr>

