<p>Pradhan Mantri Kisan Samman Nidhi (PM-KISAN) is a new Central Sector Scheme to provide income support to all landholding farmers' families in the country to supplement their financial needs for procuring various inputs related to agriculture and allied activities as well as domestic needs.

</p><br>
	 <h1 style="font-weight:bold" id="ssssss1">Eligibility</h1>
	<hr>
	<p>All land holding eligible farmer families (subject to the prevalent exclusion criteria) are to avail of the benefits under this scheme.<br>The following categories of beneficiaries of higher economic status shall not be eligible for benefit under the scheme.</p>
<br>	
<ul style="text-align:justify">
<li>• All Institutional Land holders.</li>
<li>• Farmer families in which one or more of its members belong to following categories</li>
<li>• Former and present holders of constitutional posts</li>
<li>• Former and present Ministers/ State Ministers and former/present Members of LokSabha/ RajyaSabha/ State Legislative Assemblies/ State Legislative Councils,former and present Mayors of Municipal Corporations, former and present Chairpersons of District Panchayats.</li>
<li>• All serving or retired officers and employees of Central/ State Government Ministries /Offices/Departments and its field units Central or State PSEs and Attached offices /Autonomous Institutions under Government as well as regular employees of the Local Bodies (Excluding Multi Tasking Staff /Class IV/Group D employees)</li>
<li>• All superannuated/retired pensioners whose monthly pension is Rs.10,000/-or more (Excluding Multi Tasking Staff / Class IV/Group D employees) of above category</li>
<li>• All Persons who paid Income Tax in last assessment year</li>
<li>• Professionals like Doctors, Engineers, Lawyers, Chartered Accountants, and Architects registered with Professional bodies and carrying out profession by undertaking practices.
</li>
</ul><br>



	 <h1 style="font-weight:bold" id="ssssss2">Benefits</h1>
	<hr>
	<p>Under the PM-KISAN scheme, all landholding farmers' families shall be provided the financial benefit of Rs. 6000 per annum per family payable in three equal installments of Rs. 2000 each, every four months.</p>
<br>
<h1 style="font-weight:bold" id="ssssss3">How to apply</h1>
	<hr>
<ul style="text-align:justify">
<li>• The eligible farmers may apply with the village Patwaris, revenue officials or other designated officers / agencies and submit their required details to them.</li>
<li>• Farmers can also visit their nearest <a href="https://locator.csccloud.in/"><u style="color:blue">Common Service Centres (CSCs) </u></a>for registration in the Scheme upon payment of fees.</li>
<li>• Farmers can also do their self-registration through the Farmers Corner in the <a href="https://pmkisan.gov.in/ErrorPage.aspx?status=404"><u style="color:blue"> PM KISAN portal</u></a> To check the status of the registration<a href="https://pmkisan.gov.in/GrivenceApplication/BeneficiaryStatus.aspx"><u style="color:blue"> click here.</u></a></li>
<li>• Details required for registration include  Name, Age, Gender, Category(SC/ST), Aadhaar Number (in case Aadhaar Number has not been issued then Aadhaar Enrollment Number together with any other prescribed documents for purposes of the identification such as Driving Licence, Voters’ ID Card, NREGA Job Card, or any other identification documents issued by Central/State/UT Governments or their authorities,etc.), Bank Account Number and the Mobile Number of the beneficiaries.</li>
</ul><br>
<h4>For more details visit  <a href="https://pmkisan.gov.in/ErrorPage.aspx?status=404"><u style="color:blue">PM KISAN portal</u></a></h4><br>
