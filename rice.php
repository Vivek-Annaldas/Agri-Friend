      <h4 id="list-item-3" style="color:green; font-size:30px;"><b>About Rice</b></h4>
      <p class="orgop1"><b>Oryza Sativa,</b>it is believed, is associated with wet, humid climate, though it is not a tropical plant. It is probably a descendent of wild grass that was most likely cultivated in the foothills of the far Eastern Himalayas. Another school of thought believes that the rice plant may have originated in southern India, then spread to the north of the country and then onwards to China. It then arrived in Korea, the Philippines (about 2000 B. C.) and then Japan and Indonesia (about 1000 B. C.).</br>
	  The journey of rice around the world has been slow, but once it took root it stayed and became a major agriculture and economic product for the people. In the Indian subcontinent more than a quarter of the cultivated land is given to rice (20011-12). It is a very essential part of the daily meal in the southern and eastern parts of India. In the northern and central parts of the subcontinent, where wheat is frequently eaten, rice holds its own and is cooked daily as well as on festivals and special occasions.<br/>
	  </p><h4><b>History of Rice in India </b></h4><p class="orgop1">India is an important centre of rice cultivation. The rice is cultivated on the largest areas in India. Historians believe that while the indica variety of rice was first domesticated in the area covering the foothills of the Eastern Himalayas (i.e. north-eastern India), stretching through Burma, Thailand, Laos, Vietnam and Southern China, the japonica variety was domesticated from wild rice in southern China which was introduced to India. Perennial wild rice still grow in Assam and Nepal. It seems to have appeared around 1400 BC in southern India after its domestication in the northern plains. It then spread to all the fertile alluvial plains watered by rivers. Some says that the word rice is derived from the Tamil word arisi.<br/><br/>
	  </p><h4><b>Climatic Requirements </b></h4><p class="orgop1">In India rice is grown under widely varying conditions of altitude and climate. Rice cultivation in India extends from 8 to35ºN latitude and from sea level to as high as 3000 meters. Rice crop needs a hot and humid climate. It is best suited to regions which have high humidity, prolonged sunshine and an assured supply of water. The average temperature required throughout the life period of the crop ranges from 21 to 37º C. Maximum temp which the crop can tolerate 400C to 42 0C.<br/><br/>
	  </p><h4><b>Nutritional value of Rice </b></h4><p class="orgop1">Rice is a nutritional staple food which provides instant energy as its most important component is carbohydrate (starch). On the other hand, rice is poor in nitrogenous substances with average composition of these substances being only 8 per cent and fat content or lipids only negligible, i.e., 1per cent and due to this reason it is considered as a complete food for eating. Rice flour is rich in starch and is used for making various food materials. It is also used in some instances by brewers to make alcoholic malt. Likewise, rice straw mixed with other materials is used to produce porcelain, glass and pottery. Rice is also used in manufacturing of paper pulp and livestock bedding.<br/>
	  The variability of composition and characteristics of rice is really broad and depends on variety and environmental conditions under which the crop is grown. In husked rice, protein content ranges in between 7per cent to 12per cent. The use of nitrogen fertilizers increases the percentage content of some amino acids.<br/><br/>
	  </p><h4><b>Medicinal Value </b></h4><p class="orgop1">The immense diversity of rice germplasm is a rich source for many rice based products and is also used for treating many health related maladies such as indigestion, diabetes, arthritis, paralysis, epilepsy and give strength to pregnant and lactating mothers. Ancient Ayurvedic literature testify the medicinal and curative properties of different types of rice grown in India. Medicinal rice varieties like Kanthi Banko (Chhattisgarh), Meher, Saraiphul & Danwar (Orissa), Atikaya & Kari Bhatta (Karnataka), are very common in India. Few varieties cultivated in restricted pockets of Kerala for their medical properties e.g. Chennellu, Kunjinellu, Erumakkari & Karuthachembavu etc.<br/><br/>
	  </p><h4><b>Crop Production Practices </b></h4><p class="orgop1">In India Rice is mainly grown in two types of soils i.e., (i) uplands and (ii) low lands. The method of cultivation of rice in a particular region depends largely on factors such as situation of land, type of soils, irrigation facilities, availability of labourers intensity and distribution of rainfalls. The crop of rice is grown with the following methods<br/><br/>
	  </p>
	  <h6><b>Dry or Semi-dry upland cultivation</b></h6>
	  <ol>
	  <li>Broadcasting the seed</li>
	  <li>Sowing the seed behind the plough or drilling</li>
	  </ol>
	  <h6><b>Wet or lowland cultivation</b></h6>
	  <ol>
	  <li>Transplanting in puddled fields</li>
	  <li>Broadcasting sprouted seeds in puddled fields.</li>
	  </ol>
	  <p class="orgop1"><h4><b>Selection of Seeds </b></h4></p><p class="orgop1">The use of quality seeds in cultivation of rice is an important factor to get better crop yield. Therefore, proper care has to be taken in selecting seeds of the best quality. Much of the success in raising the healthy seedlings depends on the quality of seed. Seeds intended for sowing should satisfy the following requirements</p><br/>
	  <ol>
	  <li>The seed should belong to the proper variety, which is proposed to be grown.</li>
	  <li>The seed should be clean and free from obvious mixtures of other seeds.</li>
	  <li>The seed should be mature, well developed and plump in size.</li>
	  <li>The seed should be free from obvious signs of age or bad storage.</li>
	  <li>The seed should have a high germinating capacity.</li>
	  </ol>
	  Before sowing the seed should be treated with fungicides which protects the seed against soil-born fungi and also give a boost to the seedlings.<br/>
	  </p>
	  
