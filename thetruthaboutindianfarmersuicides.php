		<h1 style="font-weight:bold; text-align:left; color:green">The Truth About Indian Farmer Suicides</h1>
<p style="text-align: justify">Rural farmers in India face incredible challenges to provide for their families. Largely ignored by India’s government, many turn to suicide. The current data on these deaths does not paint a full picture of the growing crisis.</p><br><img src="images/Indian-Farmer-1.webp" /><br><br>
<p style="text-align: justify">
Rupesh, a small-holder farmer in rural Karnataka, Southern India, was married to Neelam, with whom he had four children. He owed money to middle men who would take a cut off his new produce. Four laborers permanently worked for him, and he had employed three more seasonally. Together, the small farm sustained more than five families. Like most of their peers, they grew potatoes due to the warm weather, but the ground had been losing strength as the wells dried up quicker every year.<br><br>
Rupesh, though, seemed hopeful. Earlier this year, at a post-dinner gathering, he discussed the methods and work allotment to harvest the potatoes with his fellow laborers. After sharing half a glass of Indian rum known as ‘Old Monk’, everybody left for their homes. At midnight, Rupesh woke up in fear. It had started pouring heavily amid a hot summer. He and his wife thought the Lord of rain, Indra, was angry with them. Unfortunately, the Indian legal system refers to such an unprecedented disaster as an “act of god” rather than a consequence of climate change. 
<br><br>
The rented truck that would carry the harvest would arrive the next day. They waited till dawn as the rain reduced, and they harvested as many potatoes as possible. But the farm was muddy, and most of the produce was destroyed. The substantial debt was too much for Rupesh. To face the middle-men, he would need the strength he no longer possessed and the shame of not being able to afford  his  fast approaching daughter’s marriage was scarier than death.<br><br></p>
	<h1 style="font-weight:bold; text-align:left; color:green">Scarce Data Shifts the Narrative </h1>
	<p style="text-align: justify">This story is based on a collective reality faced by thousands of rural farmers in India, many of whom I met personally when I researched horticulture value chains in South India. India’s National Crime Records Bureau (NCRB) estimates that approximately 10,881 farmers died by suicide in 2021. But an analysis conducted by Punjab Agricultural University suggested the real number of farmer suicides in Punjab is almost five times of the NCRB data. This mismatch is a result of the politicization of data, a normalized abuse of power to nurture a favorable narrative. Which makes us wonder, is data collected for justice or to favor the institutions of justice?<br><br>

According to Section 309 of the Indian Penal Code, attempting suicide is a crime punishable by fines and imprisonment of up to one year. This punishment is highly counterproductive for farmers dying by suicide due to debt. The Mental Healthcare Act of 2017 counters Section 309, stating that any person who attempts to commit suicide shall be considered severely under stress and shall not be tried and punished under the code. However, the family of a person who dies by suicide has a legal duty to register a First Information Report with the local police. Different sociocultural factors act as barriers in this process. Someone like Neelam would probably ask, what is the point of doing all this if Rupesh has passed away?<br><br>

The resulting deficit in credible data is one of the primary reasons why farmer suicides are heavily under-reported. Rupesh’s suicide may not have been a crime legally, but families of suicide victims from under-served populations in India suffer an often-insurmountable burden of stigma. State governments collect the data from local governments, and the Union Government collects data from the State Governments through the NCRB. The data is often manipulated first at the ground level. My friend, a journalist for a local newspaper at the time, had investigated a town where a staggering number of people were dying from lightning strikes. Their investigation revealed that most of these were farmers who had died by suicide, but I doubt that this ever reached the mainstream media. It is also unclear whether NCRB data collectors consider women as farmers who are also homemakers. Similarly, suicides of Adivasis and Dalits have too been undercounted. <br><br>

The institutional architecture around this data is complex. Farmer well-being should be the responsibility of the Ministry of Agriculture & Farmer Welfare (MoA), yet the Farmer Welfare Act only addresses illicit agreements with middlemen. Until Section 309 is repealed or substantially modified, data concerning farmer suicides will be in the jurisdiction of the Ministry of Home Affairs and outside the remit of the MoA, and the stigma around suicide will continue. The Ministry of Statistics and Program Implementation (MoSPI) could have a pivotal role to play in this matter. But, the National Statistical Commission, under the MoSPI, has been recently accused of high political interference and ignorance, leading to protests and the resignation of prominent senior statisticians.<br><br>

This cynical manipulation of data continued through the Covid-19 pandemic. In 2020, Union Minister of State for Home Affairs G Kishan Reddy claimed that there was no composite  data on farmer suicides. This lack of data led to a blame game between the Union and the State governments. But it also meant they had no numbers to disagree on. So, Bharatiya Janata Party (BJP) Member of Parliament  Nishikant Dubey could claim that no farmer has died by suicide in the eight years of the administration of Prime Minister Narendra Modi. Modi had also promised in 2016 that he would double farmer income in five years. For such political powers, the truth appears to be a subjective sentiment of its supporters. <br><br></p>
<h1 style="font-weight:bold; text-align:left; color:green">The Fight for Survival Continues </h1>
<p style="text-align: justify">Even though the BJP seems to be deeply invested in preventing the public from hearing about these issues, there was a strong act of resistance from Indian farmers in response to the recurrent inequity.. In 2020, farmers across India began a mass protest movement in opposition of three new farm laws proposed by Modi’s government. According to farmers, these laws would not protect vulnerable families, but instead, negatively impact their livelihoods by conveniently bending the rules around the sale of produce. This was a much-awaited display of the civil resistance against the official preferential treatment granted to the elites, but there is still a long road ahead. <br><br>The NCRB data given to the public shows their neglect in understanding the root causes of suicide among farmers, even as 60% of the Indian population are in this group. Farmer suicides are a symptom of deep-rooted systemic issues concerning erratic supply chains, rising inequality, and governance in India.  <br><br>

Further, it is hard to imagine the number of farmers and their families that will be displaced as a consequence of climate change. Clear and concise data collection can help shape just narratives and enable farmer-centered policy-making. P. Sainath, founder editor of the People’s Archive of Rural India, argues that “the issue of farmers is not an economic issue, but an issue of civilization. What kind of society are we shaping, where one who feeds us is forced to die?”. Farmers like Rupesh and his family deserve much more. <br><br>

