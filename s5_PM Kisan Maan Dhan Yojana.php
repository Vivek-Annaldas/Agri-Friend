<p>Government has launched the Pradhan Mantri Kisan Maan DhanYojana (PM-KMY) on 12.9.2019 with a view to provide social security to Small and Marginal Farmers in their old age when they have no means of livelihood and minimal or no savings to take care of their expenses.</p><br>
	 <h1 style="font-weight:bold" id="sssss1">Benefits</h1>
	<hr>
	<p>Under this scheme, a minimum fixed pension of Rs.3,000/- is provided to the small and marginal farmers, subject to certain exclusion criteria, on attaining the age of 60 years. It is a voluntary and contributory pension scheme. The eligible farmer is required to contribute to a Pension Fund between Rs.55 to Rs.200 per month depending on the entry age. The Central Government also contributes in equal amount to the Pension Fund.</p>
<br>	
	 <h1 style="font-weight:bold" id="sssss2">Eligibility</h1>
	<hr>
	<p>1. Small and Marginal Farmer (SMF) - a farmer who owns cultivable land upto 2 hectare as per land records of the concerned State/UT.<br>2. Age of 18- 40 years<br><br><div style="font-weight:bold">Farmers who are not eligible for the scheme</div><br>The following categories of farmers have been brought under the exclusion criteria:<br></p>
	<ul style="text-align:justify">
<li>• SMFs covered under any other statuary social security schemes such as National Pension Scheme (NPS), Employees’ State Insurance Corporation scheme, Employees’ Fund Organization Scheme etc.
</li>
<li>• Farmers who have opted for Pradhan Mantri Shram Yogi Maan Dhan Yojana (PM-SYM) administered by the Ministry of Labour & Employment
</li>
<li>• Farmers who have opted for Pradhan Mantri Laghu Vyapari Maan-dhan Yojana (PM-LVM) administered by the Ministry of Labour & Employment
</li>
<li>• Further, the following categories of beneficiaries of higher economic status shall not be eligible for benefits under the scheme:</li>
<ul style="text-align:justify">

<li>• All Institutional Land holders; and</li>
<li>• Former and present holders of constitutional posts
</li>
<li>• Former and present Ministers/ State Ministers and former/present Members of Lok Sabha/ Rajya Sabha/ State Legislative Assemblies/ State Legislative Councils,former and present Mayors of Municipal Corporations, former and present Chairpersons of District Panchayats.</li>
<li>• All serving or retired officers and employees of Central/ State Government Ministries/ Offices/Departments and theirfield units,Central or State PSEs and Attached offices/ Autonomous Institutions under Government as well as regular employees of the Local Bodies (Excluding Multi Tasking Staff / Class IV/Group D employees)</li>
<li>• All Persons who paid Income Tax in last assessment year.</li>
<li>• Professionals like Doctors, Engineers, Lawyers, Chartered Accountants, and Architects registered with Professional bodies and carrying out profession by undertaking practice.
</li>
</ul>
</ul><br>
	
<h1 style="font-weight:bold" id="sssss3">How to apply</h1>
	<hr>
<h4>The enrollment to the Scheme can be done through self registration online or through the Common Service Centres in various states. The enrollment is free of cost.<br></h4>
<h4>For self enrollment through online registration, <a href="https://maandhan.in/auth/login"><u style="color:blue">click here</u></a></h4><h4>For enrollment through Common Service Centre</h4>
<h4>Farmers can visit their nearest <a href="https://locator.csccloud.in/"><u style="color:blue"> Common Service Centres (CSCs)</u></a>for registration. The Common Service Centres will charge Rs.30/- per enrolment which will be borne by the Government.</h4>
<h4>For more details visit  <a href="https://fw.pmkisan.gov.in/"><u style="color:blue">PM-KMY portal</u></a></h4><br>
