<li>• State Federations of Cooperatives</li>
<li>• State sponsored Public-Private Partnership Project</li>
</ul><br><br>
<h1 style="font-weight:bold" id="s3">Eligible projects under AIF</h1>
	<hr>
<h4 style="font-weight:bold">A. Eligible projects for all beneficiaries</h4>
	<ul style="text-align:justify">
<li>• Organic inputs production</li>
<li>• Organic inputs production
</li>
<li>• Nursery</li>
<li>• Tissue culture </li>
<li>• Seed Processing</li>
<li>• Custom Hiring Center</li>
<li>• Infrastructure for smart and precision agricultureFarm/Harvest Automation
</li>
<li>• Purchase of drones, putting up specialized sensors on field, Blockchain and AI in agriculture etc.
</li>
<li>• Remote sensing and Internet of Things (IOT) such as automatic weather station, Farm advisory services through GIS applications.
</li>
<li>• Logistics facilities - Reefer Van& Insulated vehicles
</li>
<li>• Assaying Units
</li>
<li>• Supply chain services including e-marketing platforms
</li>
<li>• Warehouse & Silos</li>
<li>• Cold Stores and Cold Chain</li>
<li>• Packaging Units</li>
<li>• Primary Processing activities</li>
</ul><br>

<h4 style="font-weight:bold">B. Eligible projects for building community farming assets</h4>
<p>In addition to above activities farmer groups such as FPOs, PACS, SHGs, JLGs, Cooperatives, National and State Level Federation of Co-operatives, FPOs federations, Federations of SHGs, National and State Level Agencies etc. are eligible for following activities:</p>
	<ul style="text-align:justify">
<li>• Hydroponic Farming </li>
<li>• Mushroom farming </li>
<li>• Vertical farming </li>
<li>• Aeroponic farming</li>
<li>• Poly house/ Greenhouse</li>
<li>• Logistics facilities(including non-refrigerated/insulated vehicles)
</li>

</ul><br>


<h1 style="font-weight:bold" id="s4">Benefits</h1>
	<hr>
	<ul style="text-align:justify">
<li>• All loans under this financing facility will have interest subvention of 3% per annum up to a limit of Rs. 2 crore. This subvention will be available for a maximum period of seven years. </li>
<li>• Further, credit guarantee coverage will be available for eligible borrowers from this financing facility under Credit Guarantee Fund Trust for Micro and Small Enterprises (CGTMSE) scheme for a loan up to Rs. 2 crore. The fee for this coverage will be paid by the Government.</li>
<li>• In case of FPOs the credit guarantee may be availed from the facility created under FPO promotion scheme of Department of Agriculture, Cooperation & Farmers Welfare (DACFW).
</li>
<li>• Moratorium for repayment under this financing facility may vary subject to minimum of 6 months and maximum of 2 years.
</li>
</ul><br>

