<li>• KYC documents of the promoter/firm/company</li>
<li>• Copy of Bank Statement for last one year (If available)</li>
<li>• Repayment track record of existing loans (Loan Statement)</li>
<li>• Net Worth Statements of promoter</li>
<li>• Detailed Project Report</li>
<li>• As applicable - Local authority permissions, Layout plans/estimates, Building sanction</li>
