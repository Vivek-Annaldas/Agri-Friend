	<h4 style="font-weight:bold">Checklist of Documents</h4>
	<ul style="text-align:justify">
<li>• Bank’s loan application form / Customer Request Letter for AIF Loan duly filled and signed
</li>
<li>• Passport size photographs of the promoter/partners/director</li>
<li>• Identity proof – Voter ID card/PAN card/Aadhaar card/Driving license</li>
<li>• Address Proof :</li>
<li>• Proof of Registration</li>
<li>• Income Tax Return for last three years, If available.</li>
<li>• Audited Balance Sheet of last 3 Years, If available.</li>
<li>• GST Certificate, if applicable.</li>
