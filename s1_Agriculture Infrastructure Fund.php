    <p>The Union Cabinet in July 2020 has approved a new pan India Central Sector Scheme called Agriculture Infrastructure Fund (National Agriculture Infra Financing Facility). The scheme shall provide a medium - long term debt financing facility for investment in viable projects for post-harvest management Infrastructure and community farming assets through interest subvention and financial support.</p>
	<br>
	<h1 style="font-weight:bold" id="s1">Scheme duration</h1>
	<hr>
	<p>The duration of the Scheme shall be from FY2020 to FY2032 (10 years).</p>
	<br><br>
	<img src="images/AIF.jpeg" /><br><br>
