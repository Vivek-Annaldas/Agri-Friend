                <h1><b>Basics of Organic Farming: Beginner Course</b></h1>
                <p><b>OFRF’s free beginning farmer training program for organic specialty crop farmers in California is now available. This online training program is for beginning farmers, existing organic farmers, and farmers in transition to organic production. While it was developed for California specialty crop farmers, the content is based on foundational principles that are relevant to all organic farmers and our hope is that growers across the U.S. find it to be a useful resource. The self-guided nature of the training program allows you to move through the readings and resources, visual and written content, and demonstration videos at your own pace.

The online training program contains six learning modules: 1) soil health, 2) weed management, 3) irrigation and water management, 4) insect and mite pest management, 5) disease management, and 6) business management and marketing.

Please help us get the word out on this new resource. We are looking for feedback and ask that anyone who takes the program also completes the brief surveys at the end.

This open educational resource is a joint effort between OFRF, the University of California Sustainable Agriculture Research and Education Program (UC SAREP), and California Polytechnic State University in San Luis Obispo, with funding from the California Department of Food and Agriculture. The self-paced program combines descriptive essays, video lectures from university faculty, and virtual field trips to demonstrate organic principles and practices.<br/><br/>
