<br/><b><h3>Areas of Cultivation :</h3></b>
Major grape-growing states are Maharashtra, Karnataka, Tamil Nadu, and Mizoram. Maharashtra ranks first in terms of production accounting for more than 70 % of total production and the highest productivity in the country during 2021-22 (3rd Advance Estimate). Karnataka is the second largest producer of Grapes with a share of 25% in 2021-22 (3rd Advance Estimate).<br/>
<br/><b><h3>India Facts and Figures :</h3></b>
