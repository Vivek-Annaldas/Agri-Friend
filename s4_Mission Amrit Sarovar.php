<p>With a view to conserve water for the future, the initiative Mission Amrit Sarovar was launched on 24th April 2022.<br>

The Mission is aimed at developing and rejuvenating 75 water bodies in each district of the country as a part of celebration of Azadi ka Amrit Mahotsav.

</p><br>
	 <h1 style="font-weight:bold" id="ssss1">Coverage</h1>
	<hr>
	<p>All rural districts will develop having at least 75 Amrit Sarovars totalling about 50,000 Amrit Sarovars in the country.</p>
<br>	
	 <h1 style="font-weight:bold" id="ssss2">Scheme duration</h1>
	<hr>
	<p>50,000 Amrit Sarovars should be completed by end of Amrit Varsh i.e. 15th August 2023.</p><br>
	
<h1 style="font-weight:bold" id="ssss3">Benefits</h1>
	<hr>
	<ul style="text-align:justify">
<li>• Amrit Sarovar will be constructed on at least 1 acre of land with a water holding capacity of about 10,000 cubic meters.
</li>
<li>• If the district is unable to create as many new Amrit Sarovars, then district may take up rejuvenation of the existing structures for restoring their ecological and productive utility.</li>
<li>• The site of Amrit Sarovars will be approved by special Gram Sabha, which will also name Panchayat Partinidhi, who will on its behalf supervise  development of Amrit Sarovar.</li>
<li>• Resources for this activity available from Mahatma Gandhi NREGS, XV Finance Commission Grants (both tied and untied), PMKSY-WDC, PMKSY-HKKP-RRR or similar schemes from the State/ Central Govt. either individually or in combination may be accessed for this purpose.</li>
<br>
