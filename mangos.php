				<h1><b>FRESH MANGOES</b></h1>
                <p>Indian mangoes come in various shape, size and color with a wide variety of flavor, aroma and taste. The Indian mango is a special product that substantiates the high standards of quality and bountiful nutrients packed in it. A single mango can provide up to 40 percent of the daily dietary fiber needs - a potent protector against heart disease, cancer and cholesterol build-up. In addition, this luscious fruit is a warehouse of potassium, beta- carotene and antioxidants. In India, mangoes are mainly grown in tropical and subtropical regions from sea level to an altitude of 1,500m. Mangoes grow best in temperatures around 27℃.<br/>
				For maintaining the highest quality standards, State-of-the-art packhouses have been set up in major production zones. Keeping in view the different country requirements, internationally recognized treatment facilities like Hot water treatment, Vapour heat treatment and Irradiation facilities have also been set up at various locations across the production belt.

A unique product identification system, compliant with the traceability networking and Residue Monitoring Plan has been developed for consumer safety and readiness for product recall in case of any emergency.<br/>
<b><h3>Varieties:</h3></b>
India is the home of about 1,000 varieties. However, only a few varieties are commercially cultivated throughout India. Most Indian mango varieties have specific eco-geographical requirements for optimum growth and yield. The Northern/Eastern Indian varieties are usually late bearing compared to Southern and Western Indian varieties. Some of the local varieties of mango bear fruits throughout the year in extreme southern parts of India.<br/>
